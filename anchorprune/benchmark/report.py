# (attr, label, kind) where kind selects the formatter.
LONG_RUN_METRICS = [
    ("lost_anchor_rate", "Lost Anchor Rate", "pct"),
    ("constraint_adherence_rate", "Constraint Adherence", "pct"),
    ("critical_conflict_quarantine_rate", "Critical Conflict Quarantine", "pct"),
    ("adversarial_contamination_rate", "Adversarial Contamination", "pct"),
    ("obsolete_payload_retention_rate", "Obsolete Retention", "pct"),
    ("milestone_retention_rate", "Milestone Retention", "pct"),
    ("final_decision_context_valid", "Final Decision Context Valid", "pct"),
    ("context_growth_slope", "Context Growth Slope (tok/step)", "num"),
    ("max_context_size", "Max Context Size (tok)", "int"),
    ("final_context_size_ratio_vs_full_history", "Final Size vs Full History", "ratio"),
    ("tokens_per_valid_context", "Tokens / Valid Context", "num"),
    ("bounded_context_score", "Bounded Context Score (experimental)", "pct"),
]


def _fmt_metric(value: Optional[float], kind: str) -> str:
    if value is None:
        return "N/A"
    if kind == "pct":
        return f"{value:.0%}"
    if kind == "int":
        return f"{int(value)}"
    if kind == "ratio":
        return f"{value:.2f}x"
    return f"{value:.2f}"  # num


def _long_run_scenario_section(
    scenario_name: str, methods: Dict[str, BenchmarkResult]
) -> str:
    present = [k for k in ORDER if k in methods]
    lines: List[str] = [f"### Scenario: `{scenario_name}`", ""]

    header = "| Metric | " + " | ".join(methods[k].method for k in present) + " |"
    sep = "| --- | " + " | ".join("---" for _ in present) + " |"
    lines += [header, sep]
    for attr, label, kind in LONG_RUN_METRICS:
        cells = []
        for k in present:
            m = methods[k]
            # A "tokens per valid context" number is only meaningful when the
            # final decision context is actually valid. Otherwise the small
            # number is a cheaper *invalid* context, not a better strategy.
            if attr == "tokens_per_valid_context" and m.final_decision_context_valid < 1.0:
                cells.append("N/A (invalid context)")
            else:
                cells.append(_fmt_metric(getattr(m, attr), kind))
        lines.append("| " + " | ".join([label] + cells) + " |")

    # Context growth by step.
    steps = max((methods[k].steps for k in present), default=0)
    lines += ["", "#### Context tokens by step", ""]
    lines += [
        "| Step | " + " | ".join(methods[k].method for k in present) + " |",
        "| ---: | " + " | ".join("---:" for _ in present) + " |",
    ]
    for i in range(steps):
        row = [str(i + 1)]
        for k in present:
            series = methods[k].context_tokens_by_step
            row.append(str(series[i]) if i < len(series) else "")
        lines.append("| " + " | ".join(row) + " |")
    lines.append("")
    return "\n".join(lines)


def build_long_run_markdown(pack: Dict[str, Dict[str, BenchmarkResult]]) -> str:
    lines: List[str] = [
        "# AnchorPrune Long-Run Benchmark Pack v0.2",
        "",
        "_Generated by `anchorprune pack`. Long-run scenarios run 10–20 steps with "
        "payloads injected over time; the evaluator is the same deterministic "
        "`MockLLM`._",
        "",
        "## Objective",
        "",
        "v0.1 showed AnchorPrune governs state correctly on short adversarial "
        "tasks. v0.2 asks a different question: **what happens to the context over "
        "a long run?** As steps accumulate, full-history memory grows without "
        "bound and lets adversarial/obsolete payloads pile up; sliding-window and "
        "summary stay small but forget anchors. The goal here is to measure "
        "context-growth slope, anchor retention over time, adversarial "
        "contamination over time, and obsolete-payload retention.",
        "",
        "## Thesis",
        "",
        "- **Full-history memory** has strong retention but **unbounded growth** "
        "and rising **adversarial contamination**.",
        "- **Sliding-window memory** is bounded but **loses anchors**.",
        "- **Simple-summary memory** is bounded but has **unstable governance**.",
        "- **AnchorPrune** maintains **governed retention** (anchors kept, "
        "adversarial state excluded) while pruning low-utility state, so growth is "
        "**slower than full history**.",
        "",
        "## New metrics",
        "",
        "- **Adversarial Contamination** — share of adversarial payloads seen so "
        "far that are present in the final context (lower is better).",
        "- **Obsolete Retention** — share of obsolete/noisy payloads still present "
        "in the final context (lower is better).",
        "- **Context Growth Slope** — least-squares slope of context tokens over "
        "steps (tokens/step). Full history should be steepest.",
        "- **Max Context Size** — largest per-step context (tokens).",
        "- **Final Size vs Full History** — final context tokens ÷ full-history "
        "final context tokens. Below `1.00x` means smaller than full history.",
        "- **Tokens / Valid Context** — `total_input_tokens ÷ "
        "number_of_valid_contexts`. Shown as **N/A** when a method has no valid "
        "contexts, and as **N/A (invalid context)** in the table when the "
        "method's *final* decision context is invalid — a small token count for "
        "an invalid context is not a cheaper strategy, it is a cheaper *invalid* "
        "context.",
        "- **Bounded Context Score** *(experimental)* — "
        "`constraint_adherence × (1 − lost_anchor) × (1 − adversarial_contamination)"
        " × (1 − normalized_growth)`. A single rough number rewarding governed "
        "retention with bounded growth; treat as directional, not precise.",
        "",
        "## Results",
        "",
    ]
    for name, methods in pack.items():
        lines.append(_long_run_scenario_section(name, methods))

    lines += [
        "## Interpretation",
        "",
        "**Token counts are only meaningful when the resulting decision context "
        "is valid.** A smaller context that has lost critical anchors or retained "
        "adversarial state is not a better memory strategy; it is a cheaper "
        "*invalid* context. That is why `Tokens / Valid Context` is reported as "
        "N/A wherever a method's final decision context is invalid — and why the "
        "`Lost Anchor Rate` and `Adversarial Contamination` rows must be read "
        "alongside any token number.",
        "",
        "**The honest framing on tokens.** AnchorPrune is *not* the smallest "
        "memory strategy — sliding-window and summary are usually smaller. It is "
        "the smallest **governed** memory strategy: the only one that keeps every "
        "critical anchor alive, quarantines adversarial overrides, and still grows "
        "more slowly than full history. Sliding-window and summary look cheap only "
        "because they have already discarded the constraints the agent must obey — "
        "cheap but blind.",
        "",
        "**Why full history fails over time.** Its context-growth slope is the "
        "steepest, and adversarial contamination climbs as override payloads "
        "accumulate and remain available to the model. Remembering everything is "
        "not the same as governing anything.",
        "",
        "**What AnchorPrune buys.** A bounded, governed context: anchors retained "
        "(lost-anchor 0%), adversarial contamination 0%, obsolete payloads pruned, "
        "milestones preserved, and a final decision context that stays valid — at "
        "a context-growth slope well below full history.",
        "",
        "## Caveats",
        "",
        "- These long-run scenarios are **deterministic and synthetic**, built to "
        "isolate context-growth and governance behavior — not real production "
        "traffic.",
        "- The evaluator is a fixed `MockLLM`; metrics reflect *what each strategy "
        "makes available in context*, not a real model's reasoning quality.",
        "- Token magnitudes are illustrative (a ~4-chars/token estimate); the "
        "*relative* growth between strategies is the signal, not absolute counts.",
        "- `Bounded Context Score` is an experimental composite; read the "
        "individual metrics for ground truth.",
        "",
    ]
    return "\n".join(lines)


def build_full_report(
    short_pack: Dict[str, Dict[str, BenchmarkResult]],
    long_pack: Dict[str, Dict[str, BenchmarkResult]],
) -> str:
    """Combined report: Part 1 (v0.1 short) + Part 2 (v0.2 long-run)."""

    return (
        build_markdown(short_pack)
        + "\n\n---\n\n"
        + build_long_run_markdown(long_pack)
    )


def build_full_results(
    short_pack: Dict[str, Dict[str, BenchmarkResult]],
    long_pack: Dict[str, Dict[str, BenchmarkResult]],
) -> dict:
    return {
        "version": "0.2",
        "short_scenarios": {
            name: {k: result_to_dict(r) for k, r in methods.items()}
            for name, methods in short_pack.items()
        },
        "long_run_scenarios": {
            name: {k: result_to_dict(r) for k, r in methods.items()}
            for name, methods in long_pack.items()
        },
    }


def build_long_run_csv(pack: Dict[str, Dict[str, BenchmarkResult]]) -> str:
    """Per-step long-run series as CSV (for future charting)."""

    rows = [
        "scenario,method,step,context_tokens,anchor_retention,"
        "adversarial_contamination,obsolete_retention,state_size"
    ]
    for scenario_name, methods in pack.items():
        for key in ORDER:
            if key not in methods:
                continue
            r = methods[key]
            for i in range(r.steps):
                rows.append(
                    ",".join(
                        str(x)
                        for x in (
                            scenario_name,
                            key,
                            i + 1,
                            _at(r.context_tokens_by_step, i),
                            _at(r.anchor_retention_by_step, i),
                            _at(r.adversarial_contamination_by_step, i),
                            _at(r.obsolete_retention_by_step, i),
                            _at(r.state_size_by_step, i),
                        )
                    )
                )
    return "\n".join(rows) + "\n"


def _at(series: List, i: int):
    return series[i] if i < len(series) else ""
