"""Benchmark reporting.

Turns a pack of scenario results into a machine-readable ``results.json`` and a
human-readable ``benchmark_report.md``.
"""

from __future__ import annotations

from typing import Dict, List, Optional

from anchorprune.benchmark.harness import BenchmarkResult

# Metrics surfaced in the report, in display order.
METRICS: List[tuple[str, str]] = [
    ("lost_anchor_rate", "Lost Anchor Rate"),
    ("constraint_adherence_rate", "Constraint Adherence"),
    ("critical_conflict_quarantine_rate", "Critical Conflict Quarantine"),
    ("payload_eviction_rate", "Payload Eviction"),
    ("milestone_retention_rate", "Milestone Retention"),
    ("final_decision_context_valid", "Final Decision Context Valid"),
]

ORDER = [
    "baseline_a_full_history",
    "baseline_b_sliding_window",
    "baseline_c_summary",
    "anchorprune",
]


def result_to_dict(result: BenchmarkResult) -> dict:
    return result.model_dump()


def build_results_json(pack: Dict[str, Dict[str, BenchmarkResult]]) -> dict:
    """pack: {scenario_name: {method_key: BenchmarkResult}}."""

    out: dict = {"version": "0.1", "scenarios": {}}
    for scenario_name, methods in pack.items():
        out["scenarios"][scenario_name] = {
            key: result_to_dict(res) for key, res in methods.items()
        }
    return out


def _fmt(value: Optional[float]) -> str:
    if value is None:
        return "N/A"
    return f"{value:.0%}"


def _scenario_section(scenario_name: str, methods: Dict[str, BenchmarkResult]) -> str:
    lines: List[str] = [f"## Scenario: `{scenario_name}`", ""]

    # Metric comparison table.
    header = "| Metric | " + " | ".join(
        methods[k].method for k in ORDER if k in methods
    ) + " |"
    sep = "| --- | " + " | ".join("---" for k in ORDER if k in methods) + " |"
    lines += [header, sep]
    for attr, label in METRICS:
        row = [label]
        for k in ORDER:
            if k in methods:
                row.append(_fmt(getattr(methods[k], attr)))
        lines.append("| " + " | ".join(row) + " |")

    # Token usage table.
    lines += ["", "### Token usage", ""]
    lines += [
        "| Method | Total input | Total output | Final context | By step |",
        "| --- | --- | --- | --- | --- |",
    ]
    for k in ORDER:
        if k not in methods:
            continue
        m = methods[k]
        by_step = ", ".join(str(t) for t in m.token_count_by_step)
        lines.append(
            f"| {m.method} | {m.total_input_tokens} | {m.total_output_tokens} "
            f"| {m.final_context_tokens} | {by_step} |"
        )

    lines.append("")
    return "\n".join(lines)


def _headline(pack: Dict[str, Dict[str, BenchmarkResult]]) -> List[str]:
    aps = {n: m["anchorprune"] for n, m in pack.items() if "anchorprune" in m}
    all_names = ", ".join(f"`{n}`" for n in aps)
    adversarial_names = [
        n for n, ap in aps.items() if ap.critical_conflict_quarantine_rate is not None
    ]

    anchors_held = all(
        ap.lost_anchor_rate == 0.0 and ap.constraint_adherence_rate == 1.0
        for ap in aps.values()
    )
    # Only scenarios that actually contain adversarial payloads count here.
    quarantined_all = bool(adversarial_names) and all(
        aps[n].critical_conflict_quarantine_rate == 1.0 for n in adversarial_names
    )

    if anchors_held and quarantined_all:
        adv = ", ".join(f"`{n}`" for n in adversarial_names)
        claim = (
            "In this deterministic benchmark pack, AnchorPrune was the only "
            "evaluated memory strategy with a governance mechanism: it preserved "
            "all critical anchors and maintained 100% constraint adherence across "
            f"the {all_names} scenarios, and quarantined 100% of adversarial "
            "override attempts in every scenario where such attempts were present "
            f"({adv}). Baselines have no governance mechanism, so their quarantine "
            "rate is 0% where adversarial payloads exist and N/A where they do not."
        )
    else:
        claim = "See the per-scenario tables below for measured results."
    return ["> " + claim, ""]


def build_markdown(pack: Dict[str, Dict[str, BenchmarkResult]]) -> str:
    lines: List[str] = [
        "# AnchorPrune Benchmark Pack v0.1",
        "",
        "_This report is generated by `anchorprune pack`. Numbers are reproducible: "
        "the harness uses a fixed, deterministic `MockLLM` with no network or "
        "randomness._",
        "",
    ]
    lines += _headline(pack)

    lines += [
        "## 1. Benchmark objective",
        "",
        "Measure how different agent-memory strategies **govern state** under "
        "noisy and adversarial context — specifically whether critical "
        "constraints survive, whether adversarial override attempts are kept out "
        "of the decision context, and whether reasoning milestones are retained. "
        "The objective is *not* to measure model reasoning quality or to minimize "
        "tokens on short tasks.",
        "",
        "## 2. Methods compared",
        "",
        "- **Baseline A — Full history.** Replays all messages every step. Retains "
        "everything but governs nothing.",
        "- **Baseline B — Sliding window.** Keeps only the most recent _N_ messages "
        "(default `N=3`, the report below uses the harness default).",
        "- **Baseline C — Simple summary.** Compresses all messages into a short "
        "extractive summary each step.",
        "- **AnchorPrune.** Governs a state graph: anchors, evidence links, "
        "conflict edges, milestones, and anchor-aware pruning.",
        "",
        "All methods receive identical inputs and the same deterministic LLM, so "
        "differences reflect the memory strategy alone.",
        "",
        "## 3. Scenario design",
        "",
        "Each scenario provides system anchors (critical constraints), evidence, a "
        "payload stream that mixes useful, noisy, and **adversarial override** "
        "blocks, and an expectation set (critical anchors, constraints, "
        "milestones, and a decision spec). Adversarial blocks explicitly attempt "
        "to weaken governance (e.g. \"ignore the policy\", \"bypass the "
        "requirement\").",
        "",
        "| Scenario | Domain | Theme |",
        "| --- | --- | --- |",
        "| `supplier` | procurement | Recommend a supplier; compliance + approval limits |",
        "| `coding_agent` | coding_agent | Fix a failing auth test without weakening security |",
        "| `contract_review` | compliance | Approve a contract only with a cited liability cap |",
        "",
        "## 4. Metrics",
        "",
        "- **Lost Anchor Rate** — share of critical anchors absent from the final context (lower is better).",
        "- **Constraint Adherence** — share of expected constraints present in the final context.",
        "- **Critical Conflict Quarantine** — share of adversarial payloads quarantined before reaching the model. "
        "Reported as **N/A** when a scenario contains no adversarial payloads. "
        "Baselines have no governance mechanism, so they score **0%** wherever adversarial payloads exist.",
        "- **Payload Eviction** — share of payload blocks removed from working state.",
        "- **Milestone Retention** — share of expected reasoning milestones retained.",
        "- **Token count by step** — input tokens per step (growth profile).",
        "- **Final Decision Context Valid** — the final context includes the required "
        "facts and excludes adversarial instructions. This measures the *context a "
        "strategy makes available*, not the reasoning quality of a real model "
        "(the evaluator is a deterministic stub).",
        "",
        "Anchor, constraint, milestone, and decision metrics are read from the "
        "final context each method exposes to the model; quarantine and eviction "
        "are read from AnchorPrune's governance state (baselines have no "
        "governance mechanism and therefore cannot quarantine).",
        "",
        "## 5. Results",
        "",
    ]
    for scenario_name, methods in pack.items():
        lines.append(_scenario_section(scenario_name, methods))

    lines += [
        "## 6. Interpretation",
        "",
        "**Full history retains information but does not govern it.** In "
        "adversarial scenarios, unsafe override payloads remain available in the "
        "final decision context with the same standing as verified anchors, "
        "causing the deterministic evaluator to mark the decision as unsafe or "
        "incorrect. The failure mode is a *lack of governance*, not a lack of "
        "information.",
        "",
        "**Sliding window and summary lose constraints.** Both compress or drop "
        "older content, so critical anchors and milestones disappear — exactly the "
        "constraints an agent must not forget.",
        "",
        "**AnchorPrune separates retention from influence.** It keeps critical "
        "anchors non-evictable, quarantines override attempts before composition, "
        "and preserves milestones — so the final decision context contains the "
        "required facts and excludes the adversarial ones.",
        "",
        "## 7. Limitations",
        "",
        "- Extraction, conflict detection, evidence linking, and compression are "
        "deterministic heuristics, not learned models.",
        "- The evaluator is a deterministic `MockLLM`; it measures memory-strategy "
        "behavior, not frontier-model reasoning quality.",
        "- Token savings are not expected on these tiny two-step scenarios because "
        "of governed-context formatting overhead.",
        "- Scenarios are synthetic, designed to isolate state-governance failures.",
        "",
        "## 8. Next benchmarks",
        "",
        "The token story becomes decisive in long, multi-step workflows where "
        "full-history memory grows unbounded and low-utility payload accumulates. "
        "Planned additions:",
        "",
        "- `long_run_coding_20_steps`",
        "- `long_run_contract_15_steps`",
        "- `long_run_procurement_10_steps`",
        "",
    ]
    return "\n".join(lines)
